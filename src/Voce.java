public class Voce {
	
	private String nome,numero;
	
     public Voce(String nome, String numero) {
		super();
		this.nome = nome;
		this.numero = numero;
	}


	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}
}
